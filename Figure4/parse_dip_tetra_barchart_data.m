    nanmean(S.laco.dimer.m.mean_ints), ...
    nanmean(S.laco.tetra.m.mean_ints), ...
    nanmean(S.cbf5.dimer.m.mean_ints), ...
    nanmean(S.cbf5.tetra.m.mean_ints) ...
    ];

m.sem_mean_ints = [ ...
    nanstd(S.laco.dimer.m.mean_ints)/sqrt(numel(S.laco.dimer.m.mean_ints)), ...
    nanstd(S.laco.tetra.m.mean_ints)/sqrt(numel(S.laco.tetra.m.mean_ints)), ...
    nanstd(S.cbf5.dimer.m.mean_ints)/sqrt(numel(S.cbf5.dimer.m.mean_ints)), ...
    nanstd(S.cbf5.tetra.m.mean_ints)/sqrt(numel(S.cbf5.tetra.m.mean_ints)) ...
    ];
