    nanmean(S.laco.dimer.m.comps), ...
    nanmean(S.laco.tetra.m.comps), ...
    nanmean(S.cbf5.dimer.m.comps), ...
    nanmean(S.cbf5.tetra.m.comps) ...
    ];

m.sem_comps =  [ ...
    nanstd(S.laco.dimer.m.comps)/sqrt(numel(S.laco.dimer.m.comps)), ...
    nanstd(S.laco.tetra.m.comps)/sqrt(numel(S.laco.tetra.m.comps)), ...
